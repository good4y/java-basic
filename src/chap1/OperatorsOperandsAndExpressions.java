package chap1;

public class OperatorsOperandsAndExpressions {
    public static void main(String[] args) {
        /*
         * Operators: 연산자
         * Operands: 피연산자
         * expression: 피연산자와 연산자로 구성된 문장

         * ex) 1 + 2
         * 1, 2: 피연산자
         * +: 연산자
         * 1 + 2: expression
         *
         * */

        char firstChar = 'A';
        char secondChar = 'B';
        System.out.println(firstChar + secondChar);
        System.out.println("" + firstChar + secondChar);
    }
}
