package chap1;

public class Variables {
    /*
    * Variables : 데이터를 저장하기 위한 메모리 공간
    * Data type : 변수에 저장할 수 있는 데이터의 종류
    * Operator : 변수와 변수 또는 변수와 상수를 연산하는 기호
    * */
    public static void main(String[] args) {
        int myFirstVariable = 10;
        System.out.println(myFirstVariable);

        myFirstVariable = 5;
        System.out.println(myFirstVariable);

        myFirstVariable = 5 * 10;
        System.out.println(myFirstVariable);
    }
}
